        function mostrar_formulario()
        {
            $("#label_req").hide();
            $('#div_formulario_contacto').removeClass("ocultar_formulario").addClass("mostrar_formulario");
            $('#nombre_completo').removeClass("input_req").addClass("fondo_input_cont");
            $('#email').removeClass("input_req").addClass("fondo_input_cont");
            $('#mensaje').removeClass("fondo_textarea_req").addClass("fondo_textarea_cont");
        }

        function ocultar_formulario()
        {
            $('#div_formulario_contacto').removeClass("mostrar_formulario").addClass("ocultar_formulario");
        }

        function save_contact_form()
        {       
            
            var nombre_completo = $("#nombre_completo").val();
            var email = $("#email").val();
            var telefono = $("#telefono").val();
            var mensaje = $("#mensaje").val();

            var fecha=moment().format('YYYY-MM-DD');
            var hora=moment().format('HH:mm:ss');
            var fecha_hora=fecha+" "+hora;

           // alert(fecha_hora);
            
            if (nombre_completo!="") {

                $('#nombre_completo').removeClass("input_req").addClass("fondo_input_cont");

                if (email!="") {

                    $('#email').removeClass("input_req").addClass("fondo_input_cont");

                    if (mensaje!="") {

                        $('#mensaje').removeClass("fondo_textarea_req").addClass("fondo_textarea_cont");

                        $("#label_req").hide();

                            $.post("ajax/index.php?op=save_contact_form",{
                                nombre_completo : nombre_completo,
                                email : email,
                                telefono : telefono,
                                mensaje : mensaje,
                                fecha_hora:fecha_hora
                                }, function(data, status)
